
public class Car
{  
	
	private String CompanyName;
	private String ModelName;
	private long year;
	private int milege;
	
	public String getCompanyName()
	{
		return CompanyName;
	}
	public void setCompanyName(String Name )
	{
		CompanyName=Name;
	}
	public String getModelName()
	{
		return ModelName;
	}
	public void setModelName(String ModName)
	{
		ModelName=ModName;
	}
	public long getYear()
	{
		return year;
	}
	public void setYear(long Y )
	{
		year=Y;
	}
	public int  getMilege()
	{
		return milege;
	}
	public void setMilege(int  M)
	{
		milege=M;
	}
	
		
}


